package sample;

public class City {
    public int id;
    public String name;
    public double pos_x;
    public double pos_y;

    public City(int id, String name, double pos_x, double pos_y) {
        this.id = id;
        this.name = name;
        this.pos_x = pos_x;
        this.pos_y = pos_y;
    }
}
